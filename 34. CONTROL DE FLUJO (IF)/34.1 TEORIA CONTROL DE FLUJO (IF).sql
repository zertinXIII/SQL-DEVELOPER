---------------------------------------------- RESUMEN DE COMANDOS -----------------------------------------------------------------------

Las estructuras de control pueden ser condicionales y repetitivas.

Estructuras de control de flujo, bifurcaciones condicionales y bucles. Veremos las condicionales. existen 2: if y case.

Existen palabras especiales que pertenecen al lenguaje de control de flujo que controlan la ejecución de las sentencias, los bloques de sentencias y procedimientos almacenados. 

Tales palabras son: "begin... end", "if... else", "while", "break" y "continue", "return" y "waitfor".

"if... else" testea una condición; se emplea cuando un bloque de sentencias debe ser ejecutado si una condición se cumple y si no se cumple, se debe ejecutar otro bloque de sentencias diferente.

Sintaxis:

 if (CONDICION) then
   SENTENCIAS-- si la condición se cumple
 else
   SENTENCIAS-- si la condición resulta falsa
 end if; 